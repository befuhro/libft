#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int nb;
	int neg;

	nb = 0;
	i = 0;
	neg = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		i++;
		neg++;
	}
	if (str[i] == '+' && str[i - 1] != '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - 48);
		i++;
	}
	if (neg == 1)
		return ((int)-nb);
	else
		return ((int)nb);
}
