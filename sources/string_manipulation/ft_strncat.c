char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	j;

	i = 0;
	j = -1;
	while (s1[i])
		i++;
	while (++j < n && s2[j])
		s1[i + j] = s2[j];
	s1[i + j] = '\0';
	return (s1);
}
